public class DDBBLista_CP {

    private int cp;
    private int idFlyers;

    public DDBBLista_CP() {

    }
    public DDBBLista_CP(int cp, int idFlyers) {
        this.cp = cp;
        this.idFlyers = idFlyers;
    }

    public int getCp() {
        return cp;
    }

    public void setCp(int cp) {
        this.cp = cp;
    }

    public int getIdFlyers() {
        return idFlyers;
    }

    public void setIdFlyers(int idFlyers) {
        this.idFlyers = idFlyers;
    }
}
